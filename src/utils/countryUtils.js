// Search countries by name
export const searchCountries = (query) => {
  try {
    if (!query || typeof query !== 'string') return [];
    
    const searchTerm = query.toLowerCase().trim();
    if (!searchTerm) return [];
    
    return allCountries.filter(country => {
      if (!country || !country.name || !country.capital || !country.continent) {
        return false;
      }
      
      try {
        return country.name.toLowerCase().includes(searchTerm) ||
               country.capital.toLowerCase().includes(searchTerm) ||
               country.continent.toLowerCase().includes(searchTerm);
      } catch (filterError) {
        console.error('Error filtering country:', country, filterError);
        return false;
      }
    });
  } catch (error) {
    console.error('Error in searchCountries:', error);
    return [];
  }
};
